//Tính toán với phần tử tăng và giảm
#include <stdio.h>
#include <stdlib.h>

int main(void) {
    system("cls");
    int a = 10, b = 100;
    float c = 10.5, d = 100.5;
    printf("++a = %d\n", ++a);
    printf("--b = %d\n", --b);
    printf("++c = %f\n", ++c);
    printf("--d = %f\n", --d);
    return 0;
}